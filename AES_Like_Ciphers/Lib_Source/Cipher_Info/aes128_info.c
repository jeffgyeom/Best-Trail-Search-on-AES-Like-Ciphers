#define xtime(x)   ((x<<1) ^ (((x>>7) & 1) * 0x1b))

#define Multiply(x,y) ((((y & 1) * x) ^ ((y>>1 & 1) * xtime(x)) ^ ((y>>2 & 1) * xtime(xtime(x))) ^ ((y>>3 & 1) * xtime(xtime(xtime(x)))) ^ ((y>>4 & 1) * xtime(xtime(xtime(xtime(x))))))&0xff)

//MDS matrix -> Shift row
void AES128_diffusion(DIFF_O_WRD_t * out, DIFF_I_WRD_t * in)
{
	int i;
	DIFF_I_WRD_t a, b, c, d;
	DIFF_O_WRD_t temp[16];

	//Mix-columns
	for (i = 0; i < 4; i++)
	{
		a = in[i * 4 + 0];
		b = in[i * 4 + 1];
		c = in[i * 4 + 2];
		d = in[i * 4 + 3];
		temp[i * 4 + 0] = Multiply(a, 0x02) ^ Multiply(b, 0x03) ^ Multiply(c, 0x01) ^ Multiply(d, 0x01);
		temp[i * 4 + 1] = Multiply(a, 0x01) ^ Multiply(b, 0x02) ^ Multiply(c, 0x03) ^ Multiply(d, 0x01);
		temp[i * 4 + 2] = Multiply(a, 0x01) ^ Multiply(b, 0x01) ^ Multiply(c, 0x02) ^ Multiply(d, 0x03);
		temp[i * 4 + 3] = Multiply(a, 0x03) ^ Multiply(b, 0x01) ^ Multiply(c, 0x01) ^ Multiply(d, 0x02);
	}

	//shift row
	out[0] = temp[0];
	out[4] = temp[4];
	out[8] = temp[8];
	out[12] = temp[12];

	out[1] = temp[5];
	out[5] = temp[9];
	out[9] = temp[13];
	out[13] = temp[1];

	out[2] = temp[10];
	out[6] = temp[14];
	out[10] = temp[2];
	out[14] = temp[6];

	out[3] = temp[15];
	out[7] = temp[3];
	out[11] = temp[7];
	out[15] = temp[11];
}

void AES128_inv_diffusion(DIFF_I_WRD_t * out, DIFF_O_WRD_t * in)
{
	int i;
	DIFF_I_WRD_t a, b, c, d;
	DIFF_I_WRD_t temp[16];

	//inverse shift row
	temp[0] = in[0];
	temp[4] = in[4];
	temp[8] = in[8];
	temp[12] = in[12];

	temp[1] = in[13];
	temp[5] = in[1];
	temp[9] = in[5];
	temp[13] = in[9];

	temp[2] = in[10];
	temp[6] = in[14];
	temp[10] = in[2];
	temp[14] = in[6];

	temp[3] = in[7];
	temp[7] = in[11];
	temp[11] = in[15];
	temp[15] = in[3];

	//Inverse Mix-columns
	for (i = 0; i < 4; i++)
	{
		a = temp[i * 4 + 0];
		b = temp[i * 4 + 1];
		c = temp[i * 4 + 2];
		d = temp[i * 4 + 3];
		out[i * 4 + 0] = Multiply(a, 0x0e) ^ Multiply(b, 0x0b) ^ Multiply(c, 0x0d) ^ Multiply(d, 0x09);
		out[i * 4 + 1] = Multiply(a, 0x09) ^ Multiply(b, 0x0e) ^ Multiply(c, 0x0b) ^ Multiply(d, 0x0d);
		out[i * 4 + 2] = Multiply(a, 0x0d) ^ Multiply(b, 0x09) ^ Multiply(c, 0x0e) ^ Multiply(d, 0x0b);
		out[i * 4 + 3] = Multiply(a, 0x0b) ^ Multiply(b, 0x0d) ^ Multiply(c, 0x09) ^ Multiply(d, 0x0e);
	}
}


