void AES128_trans_diffusion(DIFF_I_WRD_t * out, DIFF_O_WRD_t * in)
{
	int i;
	DIFF_I_WRD_t a, b, c, d;
	DIFF_I_WRD_t temp[16];

	//inverse shift row
	temp[0] = in[0];
	temp[4] = in[4];
	temp[8] = in[8];
	temp[12] = in[12];

	temp[1] = in[13];
	temp[5] = in[1];
	temp[9] = in[5];
	temp[13] = in[9];

	temp[2] = in[10];
	temp[6] = in[14];
	temp[10] = in[2];
	temp[14] = in[6];

	temp[3] = in[7];
	temp[7] = in[11];
	temp[11] = in[15];
	temp[15] = in[3];

	//trans Mix-columns
	for (i = 0; i < 4; i++)
	{
		a = temp[i * 4 + 0];
		b = temp[i * 4 + 1];
		c = temp[i * 4 + 2];
		d = temp[i * 4 + 3];
		out[i * 4 + 0] = Multiply(a, 0x02) ^ Multiply(b, 0x01) ^ Multiply(c, 0x01) ^ Multiply(d, 0x03);
		out[i * 4 + 1] = Multiply(a, 0x03) ^ Multiply(b, 0x02) ^ Multiply(c, 0x01) ^ Multiply(d, 0x01);
		out[i * 4 + 2] = Multiply(a, 0x01) ^ Multiply(b, 0x03) ^ Multiply(c, 0x02) ^ Multiply(d, 0x01);
		out[i * 4 + 3] = Multiply(a, 0x01) ^ Multiply(b, 0x01) ^ Multiply(c, 0x03) ^ Multiply(d, 0x02);
	}
}



