//Bit Permutation
//FLY64_16_WORD = 63||62||61||60||...||3||2||1||0
void FLY64_diffusion(DIFF_O_WRD_t * out, DIFF_I_WRD_t * in)
{
	uint64_t x = 0;
	int i;

	for (i = 0; i < 8; i++)
	{
		x = (x << 8) | (in[i] & 0xff);
	}

	//Benes 543210: 15 cycles, 3 masks
	bit_permute_step_64(&x, x, 0x0055005500550055, 8);
	bit_permute_step_64(&x, x, 0x0000663300006633, 16);
	bit_permute_step_64(&x, x, 0x00000000783c1e0f, 32);

	for (i = 7; i >= 0; i--)
	{
		out[i] = (DIFF_O_WRD_t)(x & 0xff);
		x = x >> 8;
	}
}

//FLY64_16_WORD = 63||62||61||60||...||3||2||1||0
void FLY64_inv_diffusion(DIFF_I_WRD_t * out, DIFF_O_WRD_t * in)
{
	uint64_t x = 0;
	int i;

	for (i = 0; i < 8; i++)
	{
		x = (x << 8) | (in[i] & 0xff);
	}

	//Benes 543210: 15 cycles, 3 masks
	bit_permute_step_64(&x, x, 0x0055005500550055, 8);
	bit_permute_step_64(&x, x, 0x0000336600003366, 16);
	bit_permute_step_64(&x, x, 0x000000000f1e3c78, 32);

	for (i = 7; i >= 0; i--)
	{
		out[i] = (DIFF_O_WRD_t)(x & 0xff);
		x = x >> 8;
	}
}

