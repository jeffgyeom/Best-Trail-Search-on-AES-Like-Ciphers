void SKINNY128_diffusion(DIFF_O_WRD_t * out, DIFF_I_WRD_t * in)
{
	DIFF_I_WRD_t temp[16];

	//Mix Columns
	temp[0] = in[0] ^ in[2] ^ in[3];
	temp[4] = in[4] ^ in[6] ^ in[7];
	temp[8] = in[8] ^ in[10] ^ in[11];
	temp[12] = in[12] ^ in[14] ^ in[15];

	temp[1] = in[0];
	temp[5] = in[4];
	temp[9] = in[8];
	temp[13] = in[12];

	temp[2] = in[1] ^ in[2];
	temp[6] = in[5] ^ in[6];
	temp[10] = in[9] ^ in[10];
	temp[14] = in[13] ^ in[14];

	temp[3] = in[0] ^ in[2];
	temp[7] = in[4] ^ in[6];
	temp[11] = in[8] ^ in[10];
	temp[15] = in[12] ^ in[14];

	//ShiftRows
	out[0] = temp[0];
	out[1] = temp[13];
	out[2] = temp[10];
	out[3] = temp[7];

	out[4] = temp[4];
	out[5] = temp[1];
	out[6] = temp[14];
	out[7] = temp[11];

	out[8] = temp[8];
	out[9] = temp[5];
	out[10] = temp[2];
	out[11] = temp[15];

	out[12] = temp[12];
	out[13] = temp[9];
	out[14] = temp[6];
	out[15] = temp[3];
}


void SKINNY128_inv_diffusion(DIFF_O_WRD_t * out, DIFF_I_WRD_t * in)
{
	DIFF_I_WRD_t temp[16];

	//Inverse ShiftRows
	temp[0] = in[0];
	temp[1] = in[5];
	temp[2] = in[10];
	temp[3] = in[15];

	temp[4] = in[4];
	temp[5] = in[9];
	temp[6] = in[14];
	temp[7] = in[3];

	temp[8] = in[8];
	temp[9] = in[13];
	temp[10] = in[2];
	temp[11] = in[7];

	temp[12] = in[12];
	temp[13] = in[1];
	temp[14] = in[6];
	temp[15] = in[11];


	//Inverse Mix Column
	out[0] = temp[1];
	out[4] = temp[5];
	out[8] = temp[9];
	out[12] = temp[13];

	out[1] = temp[1] ^ temp[2] ^ temp[3];
	out[5] = temp[5] ^ temp[6] ^ temp[7];
	out[9] = temp[9] ^ temp[10] ^ temp[11];
	out[13] = temp[13] ^ temp[14] ^ temp[15];

	out[2] = temp[1] ^ temp[3];
	out[6] = temp[5] ^ temp[7];
	out[10] = temp[9] ^ temp[11];
	out[14] = temp[13] ^ temp[15];

	out[3] = temp[0] ^ temp[3];
	out[7] = temp[4] ^ temp[7];
	out[11] = temp[8] ^ temp[11];
	out[15] = temp[12] ^ temp[15];
}

void SKINNY128_inv_trans_diffusion(DIFF_O_WRD_t * out, DIFF_I_WRD_t * in)
{
	DIFF_I_WRD_t temp[16];

	//Inverse(Transpose(Mix Columns))
	temp[0] = in[3];
	temp[4] = in[7];
	temp[8] = in[11];
	temp[12] = in[15];

	temp[1] = in[0] ^ in[1] ^ in[2];
	temp[5] = in[4] ^ in[5] ^ in[6];
	temp[9] = in[8] ^ in[9] ^ in[10];
	temp[13] = in[12] ^ in[13] ^ in[14];

	temp[2] = in[1];
	temp[6] = in[5];
	temp[10] = in[9];
	temp[14] = in[13];

	temp[3] = in[1] ^ in[2] ^ in[3];
	temp[7] = in[5] ^ in[6] ^ in[7];
	temp[11] = in[9] ^ in[10] ^ in[11];
	temp[15] = in[13] ^ in[14] ^ in[15];

	//ShiftRows
	out[0] = temp[0];
	out[1] = temp[13];
	out[2] = temp[10];
	out[3] = temp[7];

	out[4] = temp[4];
	out[5] = temp[1];
	out[6] = temp[14];
	out[7] = temp[11];

	out[8] = temp[8];
	out[9] = temp[5];
	out[10] = temp[2];
	out[11] = temp[15];

	out[12] = temp[12];
	out[13] = temp[9];
	out[14] = temp[6];
	out[15] = temp[3];
}

void SKINNY128_trans_diffusion(DIFF_O_WRD_t * out, DIFF_I_WRD_t * in)
{
	DIFF_I_WRD_t temp[16];

	//Inverse ShiftRows
	temp[0] = in[0];
	temp[1] = in[5];
	temp[2] = in[10];
	temp[3] = in[15];

	temp[4] = in[4];
	temp[5] = in[9];
	temp[6] = in[14];
	temp[7] = in[3];

	temp[8] = in[8];
	temp[9] = in[13];
	temp[10] = in[2];
	temp[11] = in[7];

	temp[12] = in[12];
	temp[13] = in[1];
	temp[14] = in[6];
	temp[15] = in[11];


	//Transpose(Mix Columns)
	out[0] = temp[0] ^ temp[1] ^ temp[3];
	out[4] = temp[4] ^ temp[5] ^ temp[7];
	out[8] = temp[8] ^ temp[9] ^ temp[11];
	out[12] = temp[12] ^ temp[13] ^ temp[15];

	out[1] = temp[2];
	out[5] = temp[6];
	out[9] = temp[10];
	out[13] = temp[14];

	out[2] = temp[0] ^ temp[2] ^ temp[3];
	out[6] = temp[4] ^ temp[5] ^ temp[7];
	out[10] = temp[8] ^ temp[9] ^ temp[11];
	out[14] = temp[12] ^ temp[14] ^ temp[15];

	out[3] = temp[0];
	out[7] = temp[4];
	out[11] = temp[8];
	out[15] = temp[12];
}


