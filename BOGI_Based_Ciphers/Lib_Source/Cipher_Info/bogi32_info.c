void BOGI32_info_out(void)
{
	int i;
	FILE* ofp;
	char name[256];
	sprintf_s(name, 256, "%s_info.txt", ALG_NAME);
	ofp = fopen(name, "w");
	fprintf(ofp, "%s\n", bogi32_commuting_types_strs[PERM_TYPE]);

	fprintf(ofp, "sidx : %d\n", SIDX);
	for (i = 0; i < NUM_SBOX_ENTRIES; i++)
	{
		fprintf(ofp, "%d, ", SBOX[i]);
	}
	fprintf(ofp, "\n");
	fprintf(ofp, "pidx : %d\n", PIDX);
	fclose(ofp);
}

